#Solicitar base y altura del triangulo
base=float(input("Introduce la base del triangulo: "))
altura=float(input("Introduce la altura del triangulo: "))
#Calcular el area
area=(base * altura)/2
#mostrar el resultado
print(f"El area del triangulo es: " + str(area)) 
