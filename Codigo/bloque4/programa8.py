# Ejemplo 1
# Imprimir los numeros del 1 al 10
i = 1 # Inicializacion de la variable de control
while i <= 10: # Condicion de parada
    print(i)
    i += 1 # Equivale a i = i + 1
# Sintaxis:
# while <condicion>:
#    <Cuerpo del while>

# Ejemplo 2
# Imprimir los numeros del 10 al 1  
i = 10
while i >= 1:
    print(i)
    i-= 1
