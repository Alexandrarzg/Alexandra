# definicion de la funcion
def saludar(nombre): # nombre es un parametro
    print(f'\n¡Hola, {nombre}!')
    print('¿Como estas?')
    print('¡Espero que te encuentres bien!\n')
    
# Llamada a la funcion
saludar('Edinguer') # Edinguer es un argumento
saludar('Chuyito') # Chuyito es un argumento
saludar('terricola') # terricola es un argumento
